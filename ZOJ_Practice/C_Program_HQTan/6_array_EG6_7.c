#include<stdio.h>
int main()
{
	char diamond[][5]={{' ',' ','*'},{' ','*',' ','*',' '},{'*',' ',' ',' ','*'},{' ','*',' ','*',' '},{' ',' ','*',' ',' '}};	//定义字符数组元素初始值，5行5列 
	int i,j;
	for(i=0;i<5;i++)	//遍历数组元素，行遍历 
		{
			for(j=0;j<5;j++)	//遍历数组元素，列遍历 
				printf("%c",diamond[i][j]);
			printf("\n");
		}
	return 0;
}
